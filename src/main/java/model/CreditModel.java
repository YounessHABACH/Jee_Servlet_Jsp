package model;

public class CreditModel {
	private double capital;
	private double taux;
	private int duree;
	private double monthlyPart;
	
	public double getCapital() {
		return capital;
	}
	public void setCapital(double capital) {
		this.capital = capital;
	}
	public double getTaux() {
		return taux;
	}
	public void setTaux(double taux) {
		this.taux = taux;
	}
	public int getDuree() {
		return duree;
	}
	public void setDuree(int duree) {
		this.duree = duree;
	}
	public double getMonthlyPart() {
		return monthlyPart;
	}
	public void setMonthlyPart(double monthlyPart) {
		this.monthlyPart = monthlyPart;
	}
	
	
}
